package com.epam.math;

/**
 * This class have different methods for basic math operations of a calculator
 * @author anubhav
 *
 */
public class Calculator {
	
	/**
	 * This method will add two integer numbers
	 * @param a
	 * @param b
	 * @return
	 */
	public int sum(int a, int b){
		return a+b;
	}
	
	/**
	 * This method will multiply two integer numbers
	 * @param a
	 * @param b
	 * @return
	 */
	public int multiply(int a, int b) {
		return a*b;
	}

	/**
	 * This method will divide first integer value by second integer value.
	 * @param a
	 * @param b
	 * @return
	 */
	public int divide(int a, int b) {
		return a/b;
	}
}
